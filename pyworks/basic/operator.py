# 연산자(operator)
# 산술 연산 - +, -, *, /, //(몫), %(나머지)
# 입력   변수 입력 또는 변수 선언
n1 = 10
n2 = 4

# 산수 연산
sum = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2



# 출력
print(n1, n2)
print(n1 + n2) #14
print(n1 - n2) #6
print(n1 * n2) #40
print(n1 / n2) #2.5
print(n1 // n2) #2
print(n1 % n2) #2
print(n1 ** n2) # 거듭제곱 10000
print(2 ** 3)

print(sum) #14
print(sub) #6
print(mul)
print(div)

